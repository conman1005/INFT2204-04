/**
 * calculate half of number
 * @param {Number} x 
 * @returns {Number} half of x
 */
export function halfOf(x = 1) {
    return x / 2;
}
/**
 * Multiply two numbers
 * @param {Number} x 
 * @param {Number} y 
 * @returns {Number} x multiplied by y
 */
export function multiply(x = 1, y = 1) {
    return x * y;
}